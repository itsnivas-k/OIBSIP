import pandas as pd
import numpy as np
import matplotlib.pyplot as plt
import seaborn as sns
from sklearn.model_selection import train_test_split
from sklearn.linear_model import LinearRegression

data = pd.read_csv("https://raw.githubusercontent.com/itsnivas-k/OIBSIP/main/Sales%20Prediction/Advertising.csv")
print(data.head())
#importing required libraries
#Linear regression analysis is used to predict the value of a variable based on the value of another variable

print(data.isnull().sum())
#cheaking is there any null values

plt.style.use('seaborn-whitegrid')
plt.figure(figsize=(12, 10))
sns.heatmap(data.corr())
plt.show()
#plotting the values in a heatmap using seaborn

x = np.array(data.drop(["Sales"], 1))
y = np.array(data["Sales"])
xtrain, xtest, ytrain, ytest = train_test_split(x, y, test_size=0.2, random_state=42)
model = LinearRegression()
model.fit(xtrain, ytrain)
ypred = model.predict(xtest)

data = pd.DataFrame(data={"Predicted Sales": ypred.flatten()})
print(data)
#predicting the sales using liner regression algorithm